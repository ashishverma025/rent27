<b>What to do at the Scene of an Accident</b>
<br><br><br>
Although we strive to operate accident free, we know that an accident can occur at any time. If you are
involved in an accident, please follow these guidelines. 

<br><br><br>
1) Secure the scene. This may consist of putting out triangles, activating the 4- ways on all vehicles,
setting out flares, (If there is no spill or volatile chemicals such as gas, diesel fuel, etc) and using other
persons for traffic control. In the case of minor accident it may be advisable to move the damaged
vehicles from the roadway. However, if doing so, please photograph or otherwise verify the location of
both vehicles prior to moving them. 

<br><br><br>
2) Render first aid and/or comfort to injured parties, if necessary. If you do not feel capable of
performing this function, try to find someone who can assist. 
<br><br><br>
3) Notify the police as quickly as possible 
<br><br><br>
4) Notify the company as quickly as possible 5) Get out your accident kit and begin obtaining all
information that is asked for on the accident report. 6) If the other party is willing to accept the blame,
ask them to fill out the driver exoneration form. 
<br><br><br>
7) Obtain the names, addresses and phone numbers of any witnesses. If people refuse to provide the
above information, record their license plate number and provide that information to the insurance
company representative. 
<br><br><br>
8) Make no statements to anyone other than the person collide with or the police, and then only
respond to questions that they ask. 
<br><br><br>
9) If you have a camera, take photographs of all aspects of the accident scene. 
<br><br><br>
10) Photograph all four sides of all vehicles involved. 
<br><br><br>
11) Photograph skid marks, gouge marks, and debris that is in the road as a result of the accident. In
these photographs, try to get some landmarks in the background to help identify the location of the
items. 

    <br><br><br>
12) Photograph any temporary situations such as illegally parked vehicles, obscured signs, etc that were
contributory to the accident
<br><br><br>
13) Photograph the other parties involved. In particular, if they seem healthy and are leaning over, or
otherwise showing back and leg mobility, photograph those actions to verify the limits of their injuries. 
<br><br><br>
14) Photograph the license plates of all vehicles stopped at the accident scene. These might very well be
witnesses that could not otherwise be discovered. 
<br><br><br>
15) Comply with the drug and alcohol testing requirements if necessary. Remember, you will be ask to
do a drug and alcohol test if;

<br><br><br>
1) it is a fatal accident, 
<br><br><br>
2) it is a personal injury accident and you received a citation or 

<br><br><br>
3) it is a property damage accident that results in one or more of the vehicles being towed and you
received a citation. 
<br><br><br>
All accidents will be evaluated by the company for preventability. Any driver who has 2 preventable
accidents in a 3 years period will lose their safety clearance. 
