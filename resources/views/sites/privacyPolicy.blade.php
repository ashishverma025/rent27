<b>Following distances:</b>
<br><br><br>
EmptyTruck100 believe in safety and recommend that all drivers are expected to use the National Safety Council’s following rule. This requires a minimum of 7 seconds of following distance at highway speeds and 6 seconds at speeds under 40mph. These following distances should be increased by at least 1 second if the roads are wet or slick.
<br><br><br>
<b>Reduced Traction:</b>
<br><br><br>
Adverse weather conditions can result in reduced traction. In the event of rain, reduce your speed by 25-30% and increase your following distance by at least 1 second. If the rain is heavy enough to require your wipers to be operated on high, or if your visibility is reduced by tire spray, speeds may need to be decreased even further and following distances increased. When operating on snow covered highways, speeds should be decreased by at least 50% and following distances should be increased by at least 2 seconds. This rule applies even if the snow cover on the highway is intermittent. Remember, there may well be icy conditions as a result of snow being compressed by traffic. When operating on icy roads, the best recommendation is “don’t.” When conditions become icy, find a safe haven and get off the road. Until you can do this, reduce your speed to a crawl, use your 4-ways, and increase your following distances by at least 2-3 seconds. Remember, ice at 32 degrees can be up to 10 times as slick as ice at 0 degrees. This means that your stopping distances can increase by a factor of 10. (As a example, on wet ice, stopping distances at 30 miles per hour can increase from a normal distance of 100 ft to as much as 800-900 ft.) This is highly recommended to protect both Empty Truck 100 and Trucking Company.
<br><br><br>
<b>Reduced visibility:</b>
<br><br><br>
Conditions such as snow, fog, heavy rain, dust, and smoke can result in reduced visibility. The rule for such conditions is to reduce your speed so that you can stop within your window of visibility. If such a speed reduction creates a hazard of being rear ended, use your 4-way flashers, stay to the right, and find a safe place to exit the highway and park as soon as possible. As a rule of thumb, make sure that you can see at least 8-10 seconds ahead of your vehicle. This gives you the opportunity to see hazards, process the information, and react without an emergency lock-up that increases the risk someone rear-ending you. Empty Truck 100 will take no responsibilities for any accident, you must abide by the TOD laws and regulations.
